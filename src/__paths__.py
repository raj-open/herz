#!/usr/bin/env python3
# -*- coding: utf-8 -*-

# ----------------------------------------------------------------
# IMPORTS
# ----------------------------------------------------------------

import os

# ----------------------------------------------------------------
# EXPORTS
# ----------------------------------------------------------------

__all__ = [
    'get_root_path',
    'get_source_path',
]

# ----------------------------------------------------------------
# CONSTANTS
# ----------------------------------------------------------------

_root = os.path.relpath(os.path.join(os.path.dirname(__file__), '..'), os.getcwd())
_source = os.path.join(_root, 'src')

# ----------------------------------------------------------------
# METHODS
# ----------------------------------------------------------------


def get_root_path() -> str:
    return _root


def get_source_path() -> str:
    return _source
