# ----------------------------------------------------------------
# IMPORTS
# ----------------------------------------------------------------

from ...thirdparty.fastapi import *

# ----------------------------------------------------------------
# EXPORTS
# ----------------------------------------------------------------

__all__ = [
    'get_security',
]

# ----------------------------------------------------------------
# LOCAL CONSTANTS, VARIABLES
# ----------------------------------------------------------------

_security = None

# ----------------------------------------------------------------
# METHODS
# ----------------------------------------------------------------


def get_security() -> HTTPBasic:
    global _security
    if not isinstance(_security, HTTPBasic):
        _security = HTTPBasic()
    return _security
